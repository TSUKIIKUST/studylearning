package com.tsuki.loopfor;

public class fordemo1 {
    //练习for循环
    public static void main(String[] args) {
        //打印1-5的整数
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
        //打印5-1
        for (int i = 5; i >= 1; i--) {
            System.out.println(i);
        }
    }
}
